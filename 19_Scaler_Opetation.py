
# SCALER OPERATION IN ARRAY IS USED TO ADD , SUBSTRACT ,MULTIPLY AND DIVIDE BY ONE VALUE TO ALL ARRAY ELEMENTS

import numpy as np

n1 = np.array([10,20,30])

n1 = n1 + 10
print(n1)


n2 = np.array([10,20,30])
n2 = n2 - 5
print(n2)

n3 = np.array([10,20,30])
n3 = n3 * 10
print(n3)


n4 = np.array([10,20,30])
n4 = n4 / 2
print(n4)